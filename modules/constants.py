def generate_song_header(title, meter, tempo_unit, tempo, key):
    """Returns a list containing the required fields in a song in ABC music format"""
    addon = 'MIDI file generated by pymprovisator, by David Asorey Álvarez (forodejazz@yahoo.es)'
    return ['X:1', 'T:"' + title + '" ' + addon , 'M:' + str(meter), 'L:1/' + str(tempo_unit), 
            'Q:1/' + str(tempo_unit) + '=' + str(tempo),'K:' + key] 

def generate_instrument_header(comment, voice, midi_channel, midi_program, *args):
    header = ['%' + comment, 'V:' + voice, '%%MIDI channel ' + midi_channel, \
            '%%MIDI program ' + midi_channel + ' ' + midi_program, ] + list(args)
    #~ print header
    return header

